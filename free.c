#include "pipex.h"

char	*ft_free_array(char **ar)
{
	int	i;

	i = 0;
	if (ar)
	{
		while (ar[i])
		{
			free(ar[i]);
			i++;
		}
		free(ar);
	}
	return (NULL);
}
